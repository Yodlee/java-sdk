package com.yodlee.sdk.sampleflow;


public class SampleAppResources {
	
	private String dnsName;
	private String userLoginName;
	private String searchSite;
	private String siteLoginField;
	private String sitePasswordField;
	
	public String getDnsName() {
		return dnsName;
	}
	
	public void setDnsName(String dnsName) {
		this.dnsName = dnsName;
	}
	
	public String getUserLoginName() {
		return userLoginName;
	}
	
	public void setUserLoginName(String userLoginName) {
		this.userLoginName = userLoginName;
	}
	
	public String getSearchSite() {
		return searchSite;
	}
	
	public void setSearchSite(String searchSite) {
		this.searchSite = searchSite;
	}
	
	public String getSiteLoginField() {
		return siteLoginField;
	}
	
	public void setSiteLoginField(String siteLoginField) {
		this.siteLoginField = siteLoginField;
	}
	
	public String getSitePasswordField() {
		return sitePasswordField;
	}
	
	public void setSitePasswordField(String sitePasswordField) {
		this.sitePasswordField = sitePasswordField;
	}
	
	
}
